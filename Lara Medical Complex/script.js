// Mobile Navigation Drawer Toggle & Scroll Highlight Link Effects
// ==========================================================================

// Scroll Event (Sticky transparent glass navbar overlay)
window.addEventListener('scroll', () => {
    if (window.scrollY > 50) {
        navbar.classList.add('scrolled');
    } else {
        navbar.classList.remove('scrolled');
    }
    
    highlightNavLink();
});

// Highlight menu links matching viewport sections
function highlightNavLink() {
    let scrollPosition = window.scrollY + 120;
    
    document.querySelectorAll('section').forEach(section => {
        if (scrollPosition >= section.offsetTop && scrollPosition < (section.offsetTop + section.offsetHeight)) {
            const currentId = section.getAttribute('id');
            navLinks.forEach(link => {
                link.classList.remove('active');
                if (link.getAttribute('href') === `#${currentId}`) {
                    link.classList.add('active');
                }
            });
        }
    });
}

// Mobile Toggle button trigger event
mobileToggle.addEventListener('click', () => {
    mobileToggle.classList.toggle('active');
    navMenu.classList.toggle('active');
});

// Auto-close menu drawer when clicking navigational links
navLinks.forEach(link => {
    link.addEventListener('click', () => {
        mobileToggle.classList.remove('active');
        navMenu.classList.remove('active');
    });
});

// ==========================================================================
// Intersection Observer (Scroll Reveal Animations Setup)
// ==========================================================================

function initScrollReveal() {
    const revealOptions = {
        threshold: 0.1,
        rootMargin: "0px 0px -40px 0px"
    };
    
    const revealObserver = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('active');
                observer.unobserve(entry.target); // Animate once on entry
            }
        });
    }, revealOptions);
    
    // Register targets
    document.querySelectorAll('.reveal, .reveal-delay-1, .reveal-delay-2, .reveal-left, .reveal-right, .reveal-card').forEach(elem => {
        revealObserver.observe(elem);
    });
}

