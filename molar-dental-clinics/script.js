// Global App State
let currentLang = localStorage.getItem('selectedLanguage') || 'en';
let currentTheme = localStorage.getItem('selectedTheme') || 'light';

// DOM Elements
const langSwitchBtn = document.getElementById('lang-switch-btn');
const langBtnText = document.getElementById('lang-btn-text');
const themeToggleBtn = document.getElementById('theme-toggle-btn');
const themeIcon = document.getElementById('theme-icon');
const navbar = document.getElementById('navbar');
const mobileToggle = document.getElementById('mobile-toggle');
const navMenu = document.getElementById('nav-menu');
const navLinks = document.querySelectorAll('.nav-link');
const bookingForm = document.getElementById('booking-form');

// Initialize Website
document.addEventListener('DOMContentLoaded', () => {
    // Apply initial language
    switchLanguage(currentLang);
    
    // Apply initial theme
    applyTheme(currentTheme);
    
    // Set min date for booking form to today
    const dateInput = document.getElementById('booking-date');
    if (dateInput) {
        const today = new Date().toISOString().split('T')[0];
        dateInput.min = today;
    }
});

// Theme Swapping Logic
function applyTheme(theme) {
    currentTheme = theme;
    localStorage.setItem('selectedTheme', theme);
    document.documentElement.setAttribute('data-theme', theme);
    
    // Update theme toggle icon
    if (theme === 'dark') {
        themeIcon.className = 'fa-solid fa-sun';
    } else {
        themeIcon.className = 'fa-solid fa-moon';
    }
}

themeToggleBtn.addEventListener('click', () => {
    const targetTheme = currentTheme === 'light' ? 'dark' : 'light';
    applyTheme(targetTheme);
});

// Switch Language Engine
function switchLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('selectedLanguage', lang);
    
    const isArabic = lang === 'ar';
    
    // 1. Update HTML attributes
    document.documentElement.lang = lang;
    document.documentElement.dir = isArabic ? 'rtl' : 'ltr';
    document.body.classList.toggle('rtl', isArabic);
    
    // 2. Translate all elements with [data-translate]
    const elementsToTranslate = document.querySelectorAll('[data-translate]');
    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang][key]) {
            element.innerHTML = translations[lang][key]; // Using innerHTML to support <br> tags in doctors section
        }
    });

    // 3. Translate SEO elements
    document.title = translations[lang].meta_title;
    document.getElementById('meta-title').textContent = translations[lang].meta_title;
    document.getElementById('meta-desc').setAttribute('content', translations[lang].meta_desc);
    document.getElementById('og-title').setAttribute('content', translations[lang].meta_title);
    document.getElementById('og-desc').setAttribute('content', translations[lang].meta_desc);
    
    // 4. Update Language switch button text
    langBtnText.textContent = isArabic ? 'English' : 'العربية';
    
    // 5. Translate Custom Tooltips / Attributes
    const elementsWithTranslateAttr = document.querySelectorAll('[data-translate-attr]');
    elementsWithTranslateAttr.forEach(element => {
        const attrMapping = element.getAttribute('data-translate-attr'); // e.g. "data-tooltip:wa-tooltip"
        const [attrName, translationKey] = attrMapping.split(':');
        if (translations[lang][translationKey]) {
            element.setAttribute(attrName, translations[lang][translationKey]);
        }
    });
    
    // 6. Update Form placeholders
    const nameInput = document.getElementById('patient-name');
    const phoneInput = document.getElementById('patient-phone');
    const serviceSelect = document.getElementById('dental-service');
    
    if (nameInput) nameInput.placeholder = isArabic ? 'الاسم الكريم' : 'Your Name';
    if (phoneInput) phoneInput.placeholder = isArabic ? 'رقم الجوال' : 'Your Phone Number';
    
    if (serviceSelect) {
        // Translate select options dynamically
        const firstOption = serviceSelect.options[0];
        firstOption.textContent = translations[lang].select_service;
        
        serviceSelect.options[1].textContent = translations[lang].service_opt_1;
        serviceSelect.options[2].textContent = translations[lang].service_opt_2;
        serviceSelect.options[3].textContent = translations[lang].service_opt_3;
        serviceSelect.options[4].textContent = translations[lang].service_opt_4;
        serviceSelect.options[5].textContent = translations[lang].service_opt_5;
        serviceSelect.options[6].textContent = translations[lang].service_opt_6;
    }
}

// Language Toggle Trigger
langSwitchBtn.addEventListener('click', () => {
    const targetLang = currentLang === 'en' ? 'ar' : 'en';
    switchLanguage(targetLang);
});

// Scroll Effects (Sticky Navbar)
window.addEventListener('scroll', () => {
    if (window.scrollY > 50) {
        navbar.classList.add('scrolled');
    } else {
        navbar.classList.remove('scrolled');
    }
    highlightNavLink();
});

// Highlight Active Nav Link based on visible sections
function highlightNavLink() {
    let scrollPosition = window.scrollY + 100;
    
    document.querySelectorAll('section').forEach(section => {
        if (scrollPosition >= section.offsetTop && scrollPosition < (section.offsetTop + section.offsetHeight)) {
            const currentId = section.getAttribute('id');
            navLinks.forEach(link => {
                link.classList.remove('active');
                if (link.getAttribute('href') === `#${currentId}`) {
                    link.classList.add('active');
                }
            });
        }
    });
}

// Mobile Menu Navigation Toggles
mobileToggle.addEventListener('click', () => {
    mobileToggle.classList.toggle('active');
    navMenu.classList.toggle('active');
});

// Close Mobile Menu when clicking on a link
navLinks.forEach(link => {
    link.addEventListener('click', () => {
        mobileToggle.classList.remove('active');
        navMenu.classList.remove('active');
    });
});

// Intersection Observer for slide/fade scroll animations
const revealOptions = {
    threshold: 0.15,
    rootMargin: "0px 0px -50px 0px"
};

const revealObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('active');
            observer.unobserve(entry.target); // Trigger animation once
        }
    });
}, revealOptions);

// Register elements to scroll-reveal
document.querySelectorAll('.reveal, .reveal-delay-1, .reveal-delay-2, .reveal-left, .reveal-right, .reveal-card').forEach(elem => {
    revealObserver.observe(elem);
});

// Booking Form Submit Logic (Redirect to WhatsApp)
bookingForm.addEventListener('submit', (e) => {
    e.preventDefault();
    
    const name = document.getElementById('patient-name').value;
    const phone = document.getElementById('patient-phone').value;
    const service = document.getElementById('dental-service').value;
    const date = document.getElementById('booking-date').value;
    const time = document.getElementById('booking-time').value;
    
    const whatsappNum = "966570707029"; // Molar Dental Clinics WhatsApp Number
    let message = "";
    
    if (currentLang === 'ar') {
        message = `مرحباً عيادات مولر لطب الأسنان، أود حجز موعد استشارة:
- الاسم: ${name}
- الجوال: ${phone}
- الخدمة المطلوبة: ${service}
- التاريخ المفضل: ${date}
- الوقت المفضل: ${time}`;
    } else {
        message = `Hello Molar Dental Clinics, I would like to book a dental consultation appointment:
- Name: ${name}
- Phone: ${phone}
- Service Required: ${service}
- Preferred Date: ${date}
- Preferred Time: ${time}`;
    }
    
    const encodedMsg = encodeURIComponent(message);
    const whatsappUrl = `https://wa.me/${whatsappNum}?text=${encodedMsg}`;
    
    // Redirect to WhatsApp in a new tab
    window.open(whatsappUrl, '_blank');
});
